package start.Practice;

public class Ex15 {
    public static void main(String[] args) {
        int count = 1;

        System.out.println("while문 시작");
        while (count <= 10) {
            System.out.println(count);
            count++;
        }

        System.out.println("for문 시작");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
